function prime(number) {
    if (number <= 1) {
        return false;
    }
    for (let i = 2; i <= Math.sqrt(number); i++) {
        if (number % i === 0) {
            return false;
        }
    }

    return true;
}

const number = 2;
const isNumberPrime = prime(number);
console.log(isNumberPrime);